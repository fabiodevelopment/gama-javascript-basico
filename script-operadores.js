// Operadores aritméticos

// Adição +
var sumTwoNumbers = 2 + 2;
console.log('sumTwoNumbers', sumTwoNumbers);

// Subtração -
var subTwoNumbers = 10 - 5;
console.log('subTwoNumbers', subTwoNumbers);

// Multiplicação *
var multTwoNumbers = 10 * 5;
console.log('multTwoNumbers', multTwoNumbers);

// Divisão /
var divtTwoNumbers = 50 / 5;
console.log('divtTwoNumbers', divtTwoNumbers);

// Módulo ou resto de uma divisão %
var restTwoNumbers = 8 % 3;
console.log('restTwoNumbers', restTwoNumbers)

// Incremento ++
var num = 1;
// console.log('num', num++) //1
console.log('++num', ++num) // num = num + 1

// Decremento --
console.log('--num', --num)

